/* 
 * bitAnd - x&y using only ~ and | 
 *   Example: bitAnd(6, 5) = 4
 *   Legal ops: ~ |
 *   Max ops: 8
 *   Rating: 1
 */
int bitAnd(int x, int y) {
  return ~((~x) | (~y));
}
/* 
 * bitXor - x^y using only ~ and & 
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14
 *   Rating: 1
 */
int bitXor(int x, int y) {
  return ~(~x & ~y) & ~(x & y);
}
/* 
 * isEqual - return 1 if x == y, and 0 otherwise 
 *   Examples: isEqual(5,5) = 1, isEqual(4,5) = 0
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int isEqual(int x, int y) {
  return !(x ^ y);
}
/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (least significant) to 3 (most significant)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
  return (x >> (n << 3)) & 0xFF;
}
/* 
 * fitsBits - return 1 if x can be represented as an 
 *  n-bit, two's complement integer.
 *   1 <= n <= 32
 *   Examples: fitsBits(5,3) = 0, fitsBits(-4,3) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 15
 *   Rating: 2
 */
int fitsBits(int x, int n) {
  // 32 - n
  int shift = 33 + ~n;
  // clears out top 32 - n bits and replaces them with bit at index n - 1
  int mask = (x << shift) >> shift;
  // return 1 if x equals mask, i.e. if twos complement of x equals twos
  // complement of mask (this means the << shift did not remove any information)
  return !(x ^ mask);
}
/* 
 * anyEvenBit - return 1 if any even-numbered bit in word set to 1
 *   where bits are numbered from 0 (least significant) to 31 (most significant)
 *   Examples anyEvenBit(0xA) = 0, anyEvenBit(0xE) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 2
 */
int anyEvenBit(int x) {
  x = (x >> 16) | x;
  x = (x >> 8) | x;

  return !!(x & 0x55); // 01010101
}
/*
 * bitCount - returns count of number of 1's in word
 *   Examples: bitCount(5) = 2, bitCount(7) = 3
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 40
 *   Rating: 4
 */
int bitCount(int x) {
  int a, b, c;

  b = 0x0F;
  b = b | (b << 8);
  b = b | (b << 16);
  // b equals 0x0F0F0F0F

  c = b ^ (b << 2); // 0x0f ^ 0x3c == 0x33
  a = c ^ (c << 1); // 0x33 ^ 0x66 == 0x55

  // a = 4x 01010101
  // b = 4x 00001111
  // c = 4x 00110011

  x = (x & a) + ((x >> 1) & a); // each consecutive two bits contain bit count
  x = (x & c) + ((x >> 2) & c); // each nibble contains bit count
  x = (x + (x >> 4)) & b; // each byte contains bit count

  x = x + (x >> 8);
  x = x + (x >> 16);

  return x & 0x3f;
}
/* 
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4 
 */
int bang(int x) {
  /*
   * x == 0 => x | -x has 0 sign bit, >> 31 gets 0x00000000, +1 gives 1
   * x != 0 => x | -x has 1 sign bit, >> 31 gets 0xFFFFFFFF, +1 gives 0
   */
  return ((x | (~x + 1)) >> 31) + 1;
}
/* 
 * leastBitPos - return a mask that marks the position of the
 *               least significant 1 bit. If x == 0, return 0
 *   Example: leastBitPos(96) = 0x20
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2 
 */
int leastBitPos(int x) {
  // https://stackoverflow.com/a/15395548/13445631
  return x & (~x + 1);
}
